	/** Create and initialize the X3D model for this object. */
	public final void initialize()
	{
  x3dModel = new X3D().setProfile(X3D.PROFILE_IMMERSIVE).setVersion(X3D.VERSION_3_3)
  .setHead(new head()
    .addMeta(new meta().setName(meta.NAME_TITLE      ).setContent("SphereSensorLefty.x3d"))
    .addMeta(new meta().setName(meta.NAME_DESCRIPTION).setContent("Using a separate SphereSensor, Lefty shark can be oriented in any direction by remote control. Navigation locked so that no other scene rotation is possible."))
    .addMeta(new meta().setName(meta.NAME_CREATOR    ).setContent("Leonard Daly and Don Brutzman"))
    .addMeta(new meta().setName(meta.NAME_CREATED    ).setContent("10 June 2006"))
    .addMeta(new meta().setName(meta.NAME_MODIFIED   ).setContent("20 October 2019"))
    .addMeta(new meta().setName(meta.NAME_WARNING    ).setContent("Xj3D not supporting SphereSensor autoOffset field correctly"))
    .addMeta(new meta().setName(meta.NAME_REFERENCE  ).setContent("https://X3dGraphics.com"))
    .addMeta(new meta().setName(meta.NAME_REFERENCE  ).setContent("https://www.web3d.org/x3d/content/examples/X3dResources.html"))
    .addMeta(new meta().setName(meta.NAME_RIGHTS     ).setContent("Copyright 2006, Leonard Daly and Don Brutzman"))
    .addMeta(new meta().setName(meta.NAME_SUBJECT    ).setContent("X3D book, X3D graphics, X3D-Edit, http://www.x3dGraphics.com"))
    .addMeta(new meta().setName(meta.NAME_IDENTIFIER ).setContent("https://X3dGraphics.com/examples/X3dForWebAuthors/Chapter08UserInteractivity/SphereSensorLefty.x3d"))
    .addMeta(new meta().setName(meta.NAME_GENERATOR  ).setContent("X3D-Edit 3.3, https://savage.nps.edu/X3D-Edit"))
    .addMeta(new meta().setName(meta.NAME_LICENSE    ).setContent("../license.html")))
  .setScene(new Scene()
    .addChild(new WorldInfo().setTitle("SphereSensorLefty.x3d"))
    .addChild(new Background().setSkyColor(new MFColor(new double[] {1.0,1.0,1.0})))
    .addChild(new NavigationInfo("LockUserSceneRotation").setType("\"NONE\""))
    .addChild(new Viewpoint().setDescription("Book View").setOrientation(0.0,-1.0,0.0,0.05).setPosition(-0.06,-0.29,8.78))
    .addChild(new Transform("UserInterfaceTranslation").setTranslation(2.0,-2.0,3.0)
      .addComments(" Note that splitting the single Transform into two, and embedding SphereSensor here, shields it from affecting other nodes in scene ")
      .addChild(new SphereSensor("Rotator").setDescription("drag sphere to rotate Lefty"))
      .addComments(" this nested Transform allows rotation about the center of the local coordinate frame ")
      .addChild(new Transform("UserInterfaceRotation")
        .addChild(new Shape()
          .setAppearance(new Appearance()
            .setMaterial(new Material().setDiffuseColor(0.0,0.0,1.0)))
          .setGeometry(new Sphere().setRadius(.5)))))
    .addComments(" note that the shark is translated to a different coordinate reference frame, so the SphereSensor is like a remote control ")
    .addChild(new Transform("SharkRotation").setTranslation(0.0,0.0,7.0)
      .addChild(new Inline().setUrl(new String[] {"../KelpForestExhibit/SharkLefty.x3d","https://X3dGraphics.com/examples/X3dForWebAuthors/KelpForestExhibit/SharkLefty.x3d","../KelpForestExhibit/SharkLefty.wrl","https://X3dGraphics.com/examples/X3dForWebAuthors/KelpForestExhibit/SharkLefty.wrl"})))
    .addChild(new ROUTE().setFromNode("Rotator").setFromField("rotation_changed").setToNode("SharkRotation").setToField("rotation"))
    .addComments(" note that the shark will rotate without this ROUTE but that user interaction feels smoother and more natural ")
    .addChild(new ROUTE().setFromNode("Rotator").setFromField("rotation_changed").setToNode("UserInterfaceRotation").setToField("rotation")));
    }
